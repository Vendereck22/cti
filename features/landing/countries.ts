const countryNameOverrides: Record<string, string> = {
  CD: "RDC",
  CG: "Congo-Brazzaville",
};

const countryNames = new Intl.DisplayNames(["fr"], { type: "region" });
const countrySorter = new Intl.Collator("fr", { sensitivity: "base" });

export function getWorldCountryOptions(): CountryOption[] {
  const priorityCodes = new Set(priorityCountries.map((country) => country.code));
  const worldCountries = WORLD_COUNTRY_CODES.filter(
    (code) => !priorityCodes.has(code)
  )
    .map(countryOptionFromCode)
    .sort((first, second) => countrySorter.compare(first.label, second.label));

  return [...priorityCountries, ...worldCountries];
}

function countryOptionFromCode(code: string): CountryOption {
  return {
    code,
    flag: countryCodeToFlagEmoji(code),
    label: countryNameOverrides[code] ?? countryNames.of(code) ?? code,
  };
}

function countryCodeToFlagEmoji(code: string) {
  return code
    .toUpperCase()
    .replace(/./g, (letter) =>
      String.fromCodePoint(127397 + letter.charCodeAt(0))
    );
}
